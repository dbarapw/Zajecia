public class PrzeciazanieMetodFunkcji {
    public int suma(int a, int b) {
        return a+b;
    }
    public float suma(float a,float b){
        return a+b;
    }

    public static void main(String[] args) {
        PrzeciazanieMetodFunkcji ob = new PrzeciazanieMetodFunkcji();
        System.out.println(ob.suma(19.0f,21.112f));
        System.out.println(ob.suma(19.0f,21.112f));
        System.out.println(ob.suma(19.0f,21.112f));
        System.out.println(ob.suma(19.0f,21.112f));
    }

}
